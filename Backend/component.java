package com.example.rcr;

public class component {
    String CA,CR;

    public component() {

    }

    public component(String CA, String CR) {
        this.CA = CA;
        this.CR = CR;
    }

    public String getCA() {
        return CA;
    }

    public void setCA(String CA) {
        this.CA = CA;
    }

    public String getCR() {
        return CR;
    }

    public void setCR(String CR) {
        this.CR = CR;
    }
}
